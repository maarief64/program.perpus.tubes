    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // mengambil data dari form
        String kodeBuku = fieldKodeBuku.getText();
        String judul = fieldJudul.getText();
        String penerbit = fieldPenerbit.getText();
        String pengarang = fieldPengarang.getText();
        String stok = fieldStok.getText();
  
        //Validasi kodebuku, judul, penerbit, pengarang, stok
        if ((kodeBuku != null && !kodeBuku.isEmpty()) 
                && (judul != null && !judul.isEmpty())
                && (penerbit != null && !penerbit.isEmpty()) 
                && (pengarang != null && !pengarang.isEmpty())
                && (stok != null && !stok.isEmpty())) {
            Integer stok2 = Integer.parseInt(stok);
            Buku buku = new Buku();
            buku.setKodeBuku(kodeBuku);
            buku.setJudul(judul);
            buku.setPenerbit(penerbit);
            buku.setPengarang(pengarang);
            buku.setStok(stok2);

            //Menambah data buku
            BukuDAO dao = new BukuDAOImpl();
            boolean sukses = dao.tambahBuku(buku);

            //Mengecek berhasil atau tidak
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data Buku berhasil ditambahkan");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data Buku gagal ditambahkan");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Silakan isi data buku");
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed


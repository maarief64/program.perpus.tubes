    private void getData(String judul) {
        BukuDAO dao = new BukuDAOImpl();
        arrayBuku = dao.getBuku(judul);

        BukuTM tm = new BukuTM();
        tm.setArrayBuku(arrayBuku);
        tableBuku.setModel(tm);
    }

    private void refreshData() {
        getData(null);
    }


    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed

        int baris = tableBuku.getSelectedRow();
        if (baris >= 0) {
            buku = arrayBuku.get(baris);
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silakan pilih Buku");
        }
    }//GEN-LAST:event_btnPilihActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed

        String judul = fieldCari.getText();
        if (judul != null && !judul.isEmpty()) {
            getData(judul);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silakan isi judul buku pada kolom pencarian");
        }
    }//GEN-LAST:event_btnCariActionPerformed


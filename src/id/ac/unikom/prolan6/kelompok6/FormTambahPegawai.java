    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // mengambil data dari form
        String username = fieldUsername.getText();
        String password = String.valueOf(fieldPassword.getPassword());
        String nip = fieldNip.getText();
        String nama = fieldNama.getText();
        String jenisKelamin;
        String akses;

        if (radioLaki.isSelected()) {
            jenisKelamin = radioLaki.getText();
        } else {
            jenisKelamin = radioPerempuan.getText();
        }
        if (radioAdmin.isSelected()) {
            akses = radioAdmin.getText();
        } else {
            akses = radioOperator.getText();
        }
        String alamat = textareaAlamat.getText();
        String noHp = fieldNoHp.getText();

        //Validasi nip, nama, jenis kelamin, alamt, no hp
        if ((username != null && !username.isEmpty())
                && (password != null && !password.isEmpty())
                && (nip != null && !nip.isEmpty()) 
                && (nama != null && !nama.isEmpty()) 
                && (jenisKelamin != null && !jenisKelamin.isEmpty())
                && (alamat != null && !alamat.isEmpty()) 
                && (noHp != null && !noHp.isEmpty())) {
            
            Pegawai pegawai = new Pegawai();
            pegawai.setUserName(username);
            pegawai.setPassWord(password);
            pegawai.setHakAkses(akses);
            pegawai.setNip(nip);
            pegawai.setNama(nama);
            pegawai.setJenisKelamin(jenisKelamin);
            pegawai.setAlamat(alamat);
            pegawai.setNoHp(noHp);

            //Memasukkan pegawai
            PegawaiDAO dao = new PegawaiDAOImpl();
            boolean sukses = dao.tambahPegawai(pegawai);

            //Mengecek berhasil atau tidak
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data pegawai berhasil ditambahkan");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data pegawai gagal ditambahkan");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Silakan isi data pegawai");
        }

    }//GEN-LAST:event_buttonSimpanActionPerformed

    private void fieldNipKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldNipKeyPressed
        int key = evt.getKeyCode();
        if ((key >= evt.VK_0 && key <= evt.VK_9) 
                || (key >= evt.VK_NUMPAD0 && key <= evt.VK_NUMPAD9)
                || (key == KeyEvent.VK_BACK_SPACE))  {
            fieldNip.setEditable(true);
            fieldNip.setBackground(Color.white);
        } else {
            fieldNip.setEditable(false);
            fieldNip.setBackground(Color.red);
        }
    }//GEN-LAST:event_fieldNipKeyPressed


    private void getRiwayat() {
        dao = new PeminjamanDAOImpl();
        arrayPeminjaman = dao.getRiwayat();

        RiwayatTM tm = new RiwayatTM();
        tm.setArrayPeminjaman(arrayPeminjaman);

        tableRiwayat.setModel(tm);
    }

    private void cariData(String keyword) {
        dao = new PeminjamanDAOImpl();
        arrayPeminjaman = dao.cariPengembalian(keyword);

        PeminjamanTM tm = new PeminjamanTM();
        tm.setArrayPeminjaman(arrayPeminjaman);

        tableRiwayat.setModel(tm);
    }


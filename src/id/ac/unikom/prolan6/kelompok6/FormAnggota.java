    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int baris = tableAnggota.getSelectedRow();
        if (baris >= 0) {

            //Mengambil anggota dari baris tabel anggota
            Anggota anggotaTerpilih = arrayAnggota.get(baris);

            //Memunculkan form anggota
            FormUbahAnggota fup = new FormUbahAnggota(null, true, anggotaTerpilih);
            fup.setLocationRelativeTo(null);
            fup.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Pilih anggota yang akan diubah");
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = tableAnggota.getSelectedRow();
        if (baris >= 0) {
            //Mengambil anggota terpilih dari tabel anggota
            Anggota AnggotaTerpilih = arrayAnggota.get(baris);

            int aksiUser = JOptionPane.showConfirmDialog(this, "Apakah Anda "
                    + "yakin akan menghapus data "
                    + AnggotaTerpilih.getNama(), "Hapus Anggota",
                    JOptionPane.YES_NO_OPTION);

            if (aksiUser == JOptionPane.YES_OPTION) {
                String nim = AnggotaTerpilih.getNim();
                AnggotaDAO dao = new AnggotaDAOImpl();
                boolean sukses;
                sukses = dao.hapusAnggota(nim);
                if (sukses) {
                    JOptionPane.showMessageDialog(this, "Anggota berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Anggota gagal dihapus");
                }
            }
            getData(null);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih anggota yang akan dihapus");
        }
        getData(null);
    }//GEN-LAST:event_btnHapusActionPerformed


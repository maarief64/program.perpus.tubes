    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed

        String nama = fieldCari.getText();
        if (nama != null && !nama.isEmpty()) {
            getData(nama);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silakan isi nama anggota pada kolom pencarian");
        }

    }//GEN-LAST:event_btnCariActionPerformed

    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed
        int baris = tableAnggota.getSelectedRow();
        if (baris >= 0) {
            anggota = arrayAnggota.get(baris);
            dispose();
        } else {
             JOptionPane.showMessageDialog(rootPane, "Silakan pilih Anggota");
        }

    }//GEN-LAST:event_btnPilihActionPerformed


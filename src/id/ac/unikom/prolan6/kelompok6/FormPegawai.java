    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int baris = tablePegawai.getSelectedRow();
        if (baris >= 0) {

            // mengambil anggota dari baris table
            Pegawai pegawaiTerpilih = arrayPegawai.get(baris);

            // munculkan form
            FormUbahPegawai fup = new FormUbahPegawai(null, true, pegawaiTerpilih);
            fup.setLocationRelativeTo(null);
            fup.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Pilih pegawai yang akan diubah");
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = tablePegawai.getSelectedRow();
        if (baris >= 0) {
            Pegawai pegawaiTerpilih = arrayPegawai.get(baris);

            int aksiUser = JOptionPane.showConfirmDialog(this, "Apakah Anda "
                    + "yakin akan menghapus data "
                    + pegawaiTerpilih.getNama(), "Hapus Anggota",
                    JOptionPane.YES_NO_OPTION);

            if (aksiUser == JOptionPane.YES_OPTION) {
                String nip = pegawaiTerpilih.getNip();
                PegawaiDAO dao = new PegawaiDAOImpl();
                boolean sukses;
                sukses = dao.hapusPegawai(nip);
                if (sukses) {
                    JOptionPane.showMessageDialog(this, "Pegawai berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Pegawai gagal dihapus");
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Pilih pegawau yang akan dihapus");
        }
        getData(null);
    }//GEN-LAST:event_btnHapusActionPerformed


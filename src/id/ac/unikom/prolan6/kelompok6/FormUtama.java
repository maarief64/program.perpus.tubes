    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // mengambil data
        String username = fieldUsername.getText();
        String password = String.valueOf(fieldPassword.getPassword());
        String hakAkses;
        
        if (radioAdmin.isSelected()) {
            hakAkses = radioAdmin.getText();
        } else {
            hakAkses = radioOperator.getText();
        }

        //validasi Input Data
        if (username.isEmpty() || password.isEmpty() || hakAkses.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Isi Form Login");
        } else {

            // proses login
            PegawaiDAO dao = new PegawaiDAOImpl();
            pegawai = dao.login(username, password, hakAkses);
            if (pegawai != null) {
                hakakses = pegawai.getHakAkses();
            } 
            // cek hasil login
            if (pegawai != null && hakakses.equalsIgnoreCase("Admin")) {
                JOptionPane.showMessageDialog(this,
                        "Login berhasil : " + pegawai.getNama());
                String nip1 = pegawai.getNip();
                btnAnggota.setEnabled(true);
                btnBuku.setEnabled(true);
                btnKembali.setEnabled(false);
                btnPegawai.setEnabled(true);
                btnPinjam.setEnabled(false);
                btnLogin.setEnabled(false);
                btnRiwayat.setEnabled(true);
                btnLogout.setVisible(true);
                lblName.setText(pegawai.getNama());

            } else if (pegawai != null && hakakses.equalsIgnoreCase("Operator")) {
                JOptionPane.showMessageDialog(this,
                        "Login berhasil : " + pegawai.getNama());
                String nip1 = pegawai.getNip();
                btnPegawai.setEnabled(false);
                btnAnggota.setEnabled(false);
                btnBuku.setEnabled(false);
                btnKembali.setEnabled(true);
                btnPegawai.setEnabled(false);
                btnPinjam.setEnabled(true);
                btnLogin.setEnabled(false);
                btnLogout.setVisible(true);
                btnRiwayat.setEnabled(true);
                lblName.setText(pegawai.getNama());
            } else {
                JOptionPane.showMessageDialog(this,
                        "Login gagal");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed


    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String fNim = fieldNim.getText();
        String fNama = fieldNama.getText();
        String fKodeBuku = fieldKode.getText();
        String fJudul = fieldJudul.getText();
        Calendar tgl = datePinjam.getCalendar();
        if ((fNim != null && !fNim.isEmpty()) && (fNama != null && !fNama.isEmpty())
                && (fKodeBuku != null && !fKodeBuku.isEmpty()) && (fJudul != null && !fJudul.isEmpty())) {
            if (tgl != null) {
                simpan();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Tanggal peminjaman harus diisi!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Data peminjaman harus diisi!");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


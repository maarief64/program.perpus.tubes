    private void simpan() {
        // ambil tanggal peminjaman
        tglPinjam = datePinjam.getCalendar();

        if (stok > 0) {
            stok = stok - 1;
            buku.setStok(stok);
            buku.setKodeBuku(kodeBuku);
        } else {
            JOptionPane.showMessageDialog(this, "Stok buku habis");
        }
            
        // proses insert dan update stok
        PeminjamanDAO dao = new PeminjamanDAOImpl();
        BukuDAO dao2 = new BukuDAOImpl();
        boolean sukses = dao.pinjam(anggota.getNim(), buku.getKodeBuku(), tglPinjam, nip);
        boolean sukses2 = dao2.ubahStok(buku);
        if (sukses) {
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Peminjaman gagal dilakukan");
        }
    }

